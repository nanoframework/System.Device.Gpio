namespace Windows.Devices.Gpio
{
    /// <summary>
    /// Describes the possible values for a general-purpose I/O (GPIO) pin.
    /// </summary>
    public enum GpioPinValue
    {
        /// <summary>
        /// The value of the GPIO pin is low.
        /// </summary>
        Low = 0,
        /// <summary>
        /// The value of the GPIO pin is high.
        /// </summary>
        High
    }
}
